function app()
	{
		document.getElementById("opd_btn_sec").style.display="block";
		document.getElementById("opd").style.display="none";
		document.getElementById("app").style.display="block";
		document.getElementById("app_btn_sec").style.display="none";
		
	}

	function opd()
	{
		document.getElementById("opd_btn_sec").style.display="none";
		document.getElementById("opd").style.display="block";
		document.getElementById("app").style.display="none";
		document.getElementById("app_btn_sec").style.display="block";
		
	}
